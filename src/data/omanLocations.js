const ALL_WILAYATS = Object.values(WILAYATS).flat()

const LEGACY_WILAYAT_IDS = {
  jalan: 'jalan-bani-bu-ali',
}

function isArabicLang(lang) {
  return lang === 'ar' || lang === 'ur'
}

export function getGovernorateLabel(gov, lang) {
  if (typeof gov === 'string') {
    return getGovernorateNameById(gov, lang)
  }
  if (isArabicLang(lang)) return gov.nameAr
  return gov.nameEn
}

export function getWilayatLabel(wilayat, lang) {
  if (typeof wilayat === 'string') {
    return getWilayatNameById(wilayat, lang)
  }
  if (isArabicLang(lang)) return wilayat.nameAr
  return wilayat.nameEn
}

export function getGovernorateNameById(id, lang = 'ar') {
  const gov = GOVERNORATES.find((item) => item.id === id)
  if (!gov) return id || 'غير محدد'
  return isArabicLang(lang) ? gov.nameAr : gov.nameEn
}

export function getWilayatNameById(id, lang = 'ar') {
  const resolvedId = LEGACY_WILAYAT_IDS[id] || id
  const wilayat = ALL_WILAYATS.find((item) => item.id === resolvedId)
  if (!wilayat) return id || 'غير محدد'
  return isArabicLang(lang) ? wilayat.nameAr : wilayat.nameEn
}

export function getWilayatsForGovernorate(governorateId) {
  return WILAYATS[governorateId] || []
}
